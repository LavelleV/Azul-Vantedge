export function getHowertHomeProgramByNumber(
  programNumber: number
): HowertHomeProgram | undefined {
  return HOWERT_HOME_PROGRAMS.find(
    (program) => program.programNumber === programNumber
  );
}

export function getHowertHomeProgramByCode(
  protocolCode: string
): HowertHomeProgram | undefined {
  const normalizedCode = protocolCode.trim().toLowerCase();

  return HOWERT_HOME_PROGRAMS.find(
    (program) => program.protocolCode.toLowerCase() === normalizedCode
  );
}

export function getHowertHomeProgramsByModule(
  moduleId: HowertHomeModuleId
): HowertHomeProgram[] {
  return HOWERT_HOME_PROGRAMS.filter((program) => program.moduleId === moduleId);
}

export function searchHowertHomePrograms(query: string): HowertHomeProgram[] {
  const normalizedQuery = query.trim().toLowerCase();

  if (!normalizedQuery) {
    return [];
  }

  return HOWERT_HOME_PROGRAMS.filter((program) => {
    const searchableText = [
      program.programNumber,
      program.protocolCode,
      program.moduleName,
      program.programName,
      program.usesFor,
      program.suggestedFrequency,
      program.precautions,
      program.setups.join(" "),
      program.tags.join(" "),
      program.notes?.join(" ") ?? "",
    ]
      .join(" ")
      .toLowerCase();

    return searchableText.includes(normalizedQuery);
  });
}

export function findHowertHomeProgramsByTags(tags: string[]): HowertHomeProgram[] {
  const normalizedTags = tags.map((tag) => tag.trim().toLowerCase()).filter(Boolean);

  if (!normalizedTags.length) {
    return [];
  }

  return HOWERT_HOME_PROGRAMS.filter((program) =>
    normalizedTags.some((tag) =>
      program.tags.some((programTag) => programTag.toLowerCase().includes(tag))
    )
  );
}
